def _primary_route_card(cards: list[dict[str, Any]]) -> dict[str, Any]:
    """
    [ACTION]
    Pick the atlas-ordered first command for a task class.

    The family/atlas order is already curated. Do not silently re-rank routes
    by evidence strength; the task row remains a selector, not a maturity score.

    - Teleology: pick the atlas-ordered first organ as a task class's primary
      route, deliberately honoring curated order over evidence strength.
    - Guarantee: returns ``cards[0]`` unchanged; never reorders by rank/score.
    - Fails: raises ``IndexError`` if ``cards`` is empty (callers pass non-empty
      task-class card lists).
    - Non-goal: does not rank by maturity or authorize the primary as a quality
      or release signal.
    - Preconditions: Caller supplies arguments satisfying the signature plus any path, schema, state, or type constraints enforced by the body.
    - Reads: call arguments, module constants, imported helpers.
    - Writes: return values.
    """
    return cards[0]


def render_agent_task_routes_json(model: dict[str, Any]) -> dict[str, Any]:
    """
    [ACTION]
    Build the generated agent task-route read model from atlas cards.

    - Teleology: project the merged model into the GENERATED
      ``atlas/agent_task_routes.json`` read model that selects organs by task
      class for agents.
    - Guarantee: returns a dict with ``schema_version``, ``source_refs``,
      ``generation`` (do-not-hand-edit marker), per-task ``routes`` with a
      primary organ + relevant organs + source-relation handles, the glance
      ladder, and capsule accounting.
    - Fails: never raises for a passing model; ``KeyError`` if called on a
      malformed model lacking ``families``/``coverage``.
    - When-needed: inspect when building the routes file or debugging which organ
      a task class resolves to.
    - Reads: ``model['families']`` cards and ``model['coverage']``.
    - Writes: nothing directly; ``build(write=True)`` serializes this to
      ``atlas/agent_task_routes.json``.
    - Escalates-to: ``scripts/build_organ_atlas.py --write`` to regenerate;
      ``load_model`` for the upstream source registries.
    - Non-goal: the projection is not source authority and is not a release,
      external-model, source-change, or whole-system correctness decision.
    - Preconditions: Caller supplies arguments satisfying the signature plus any path, schema, state, or type constraints enforced by the body.
    """
    by_task: dict[str, list[dict[str, Any]]] = {}
    for fam in model["families"]:
        for card in fam["cards"]:
            for task_class in card.get("specialty") or []:
                by_task.setdefault(str(task_class), []).append(card)

    routes: list[dict[str, Any]] = []
    for task_class in sorted(by_task):
        cards = by_task[task_class]
        primary = _primary_route_card(cards)
        primary_receipts = [
            str(ref)
            for ref in primary.get("generated_receipts", [])
            if str(ref).strip()
        ]
        receipt_ref = str(primary.get("acceptance") or "").strip()
        if not receipt_ref and primary_receipts:
            receipt_ref = primary_receipts[0]
        relevant_organs = []
        for card in cards:
            receipts = [
                str(ref)
                for ref in card.get("generated_receipts", [])
                if str(ref).strip()
            ]
            organ_source_summary = card.get("source_relation_summary")
            scope_limit = _public_scope_text(card.get("claim_ceiling_restated"))
            relevant_organs.append(
                {
                    "organ_id": card["organ_id"],
                    "display_name": card.get("display_name") or card["organ_id"],
                    "family": card.get("family"),
                    "evidence_class": card.get("evidence_class"),
                    "evidence_strength_rank": card.get("evidence_strength_rank"),
                    "one_line": _public_scope_text(card.get("one_line")),
                    "compression_card": _public_scope_text(
                        card.get("compression_card")
                    ),
                    "compression_authority_ceiling": _public_scope_text(
                        card.get("compression_authority_ceiling")
                    ),
                    "capsule_id": card.get("capsule_id"),
                    "capsule_join_status": card.get("capsule_join_status"),
                    "first_command": card.get("first_command"),
                    "scope_limit": scope_limit,
                    "claim_ceiling_restated": scope_limit,
                    "authority_boundary": scope_limit,
                    "receipt_refs": receipts,
                    "acceptance_ref": card.get("acceptance"),
                    "standard_ref": card.get("standard"),
                    "paper_module_ref": card.get("paper_module"),
                    "concept_ref": card.get("concept_projection"),
                    "mechanism_ref": card.get("mechanism_projection"),
                    "drilldown_target": _organ_anchor(card),
                    "wires_to": card.get("wires_to") or [],
                    "source_relation_handle": _source_relation_route_handle(
                        str(card["organ_id"]),
                        organ_source_summary
                        if isinstance(organ_source_summary, dict)
                        else None,
                    ),
                }
            )
        source_relation_summaries = [
            card["source_relation_summary"]
            for card in cards
            if isinstance(card.get("source_relation_summary"), dict)
        ]
        route_source_relation_summary = _route_source_relation_summary(
            task_class,
            source_relation_summaries,
        )
        if not route_source_relation_summary.get("query_examples"):
            route_source_relation_summary["query_examples"] = [
                _organ_topology_command("--organ", str(primary["organ_id"]))
            ]
            route_source_relation_summary["fallback_reason"] = (
                "no_source_module_edges_recorded_for_relevant_organs"
            )
        routes.append(
            {
                "task_class": task_class,
                "route_role": "agent_task_class_to_organ_selector",
                "organ_count": len(cards),
                "primary_organ_id": primary["organ_id"],
                "primary_display_name": primary.get("display_name")
                or primary["organ_id"],
                "relevant_organs": relevant_organs,
                "first_command": primary.get("first_command"),
                "allowed_scope": _public_scope_text(primary.get("claim_ceiling_restated")),
                "evidence_ref": (
                    f"core/organ_registry.json::implemented_organs"
                    f"[organ_id={primary['organ_id']}]"
                ),
                "receipt_ref": receipt_ref,
                "source_relation_summary": route_source_relation_summary,
                "stop_condition": (
                    "Stop when the first command or named result record is visible, "
                    "the selected component card is opened, and the card's scope "
                    "limit is named before any broader conclusion."
                ),
                "drilldown_target": _organ_anchor(primary),
                "authority_boundary": (
                    "Task routing only. This row selects public components from the "
                    "frozen atlas; its scope is local task selection, source-linked "
                    "records, and component-specific evidence."
                ),
            }
        )

    return {
        "schema_version": "microcosm_agent_task_routes_v1",
        "surface_role": "generated_agent_task_route_projection",
        "source_refs": [
            REGISTRY_REL,
            FAMILIES_REL,
            GLOSSES_REL,
            EVIDENCE_CLASSES_REL,
            PAPER_MODULE_CAPSULES_REL,
            "atlas/entry_packet.json::concept_mechanism_entry_route",
            "core/public_standard_pressure.json",
            SOURCE_MODULE_FILE_GRAPH_REF,
        ],
        "generation": {
            "do_not_hand_edit": (
                "Generated by PYTHONPATH=src python3 scripts/build_organ_atlas.py "
                "--write from the frozen organ atlas model."
            ),
            "source_model": "microcosm_core.projections.organ_atlas.load_model",
            "source_relation_model": (
                "microcosm_core.projections.organ_surface_contract."
                "build_organ_surface_contract"
            ),
        },
        "route_count": len(routes),
        "accepted_organ_count": model["coverage"]["registry_organ_count"],
        "organ_glance_ladder": _organ_glance_ladder(model),
        "capsule_accounting": model.get("capsule_accounting"),
        "source_relation_route_count": len(routes),
        "source_relation_nonzero_edge_route_count": sum(
            1
            for route in routes
            if route["source_relation_summary"].get("edge_count", 0) > 0
        ),
        "source_relation_summary": model.get("source_relation_summary"),
        "authority_boundary": (
            "Agent task routes are selectors over accepted public organ cards. "
        "Scope remains on each component's registry row, generated result records, "
        "and public scope limit."
        ),
        "routes": routes,
    }


