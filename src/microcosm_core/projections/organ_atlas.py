def build(root: str | Path, *, write: bool) -> dict[str, Any]:
    """Build the atlas model and optionally write generated atlas surfaces.

    In ``--check`` callers, pass ``write=False`` and compare the returned
    rendered outputs to the on-disk files.
    """
    root = Path(root)
    model = load_model(root)
    organs_md = render_organs_md(model)
    architecture_md = render_architecture_md(model)
    agent_routes_md = render_agent_routes_md(model)
    agent_task_routes = render_agent_task_routes_json(model)
    agent_task_routes_json = (
        json.dumps(agent_task_routes, ensure_ascii=True, indent=2, sort_keys=True)
        + "\n"
    )
    on_disk_organs = (root / ORGANS_MD_REL).read_text(encoding="utf-8") if (root / ORGANS_MD_REL).is_file() else None
    on_disk_arch = (root / ARCHITECTURE_MD_REL).read_text(encoding="utf-8") if (root / ARCHITECTURE_MD_REL).is_file() else None
    on_disk_agent_routes = (root / AGENT_ROUTES_MD_REL).read_text(encoding="utf-8") if (root / AGENT_ROUTES_MD_REL).is_file() else None
    agent_task_routes_path = root / AGENT_TASK_ROUTES_JSON_REL
    on_disk_agent_task_routes = agent_task_routes_path.read_text(encoding="utf-8") if agent_task_routes_path.is_file() else None
    drift = []
    if on_disk_organs is not None and on_disk_organs != organs_md:
        drift.append(ORGANS_MD_REL)
    if on_disk_arch is not None and on_disk_arch != architecture_md:
        drift.append(ARCHITECTURE_MD_REL)
    if on_disk_agent_routes is not None and on_disk_agent_routes != agent_routes_md:
        drift.append(AGENT_ROUTES_MD_REL)
    if (
        on_disk_agent_task_routes is not None
        and on_disk_agent_task_routes != agent_task_routes_json
    ):
        drift.append(AGENT_TASK_ROUTES_JSON_REL)
    wrote: list[str] = []
    if write and model["status"] == "pass":
        (root / ORGANS_MD_REL).write_text(organs_md, encoding="utf-8")
        (root / ARCHITECTURE_MD_REL).write_text(architecture_md, encoding="utf-8")
