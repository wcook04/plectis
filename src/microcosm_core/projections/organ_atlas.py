def build(root: str | Path, *, write: bool) -> dict[str, Any]:
    """Build the atlas model and optionally write ORGANS.md/ARCHITECTURE.md.

    In ``--check`` callers, pass ``write=False`` and compare the returned
    ``organs_md``/``architecture_md`` to the on-disk files.
    """
    root = Path(root)
    model = load_model(root)
    organs_md = render_organs_md(model)
    architecture_md = render_architecture_md(model)
    on_disk_organs = (root / ORGANS_MD_REL).read_text(encoding="utf-8") if (root / ORGANS_MD_REL).is_file() else None
    on_disk_arch = (root / ARCHITECTURE_MD_REL).read_text(encoding="utf-8") if (root / ARCHITECTURE_MD_REL).is_file() else None
    drift = []
    if on_disk_organs is not None and on_disk_organs != organs_md:
        drift.append(ORGANS_MD_REL)
    if on_disk_arch is not None and on_disk_arch != architecture_md:
        drift.append(ARCHITECTURE_MD_REL)
    wrote: list[str] = []
    if write and model["status"] == "pass":
        (root / ORGANS_MD_REL).write_text(organs_md, encoding="utf-8")
        (root / ARCHITECTURE_MD_REL).write_text(architecture_md, encoding="utf-8")
