def _organ_glance_ladder(model: dict[str, Any]) -> list[dict[str, Any]]:
    families: list[dict[str, Any]] = []
    for fam in model.get("families", []):
        family_label = str(fam.get("label") or fam.get("family_id") or "")
        families.append(
            {
                "family_id": fam.get("family_id"),
                "label": family_label,
                "organ_count": fam.get("organ_count"),
                "organs": [
                    _organ_glance_row(card, family_label)
                    for card in fam.get("cards", [])
                ],
            }
        )
