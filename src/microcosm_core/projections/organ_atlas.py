def _rows(payload: Any, key: str) -> list[dict[str, Any]]:
    if not isinstance(payload, dict):
        return []
    return [row for row in payload.get(key, []) if isinstance(row, dict)]


def _accepted_registry_rows(root: Path) -> list[dict[str, Any]]:
    registry = read_json_strict(root / REGISTRY_REL)
    return [
        row
        for row in _rows(registry, "implemented_organs")
        if row.get("status") == "accepted_current_authority"
    ]


def _paper_module_ref(root: Path, organ_id: str, gloss: dict[str, Any]) -> str | None:
    """Best-effort paper-module path for an organ (used for drilldown links)."""
    declared = str(gloss.get("paper_module_ref") or "").strip()
    if declared:
        if (root / declared).is_file():
            return declared
        declared_path, _, fragment = declared.partition("#")
        if fragment and (root / declared_path).is_file():
            capsule_doc = read_json_strict(root / declared_path)
            for row in _rows(capsule_doc, "paper_modules"):
                if str(row.get("id") or "") != fragment:
                    continue
                projection = str(row.get("legacy_markdown_projection") or "").strip()
                if projection and (root / projection).is_file():
                    return projection
                return declared
        return None
    direct = Path("paper_modules") / f"{organ_id}.md"
    if (root / direct).is_file():
        return direct.as_posix()
    return None


def _paper_module_capsules(root: Path) -> list[dict[str, Any]]:
    path = root / PAPER_MODULE_CAPSULES_REL
    if not path.is_file():
        return []
    payload = read_json_strict(path)
    return _rows(payload, "paper_modules")


def _capsule_index(capsules: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    """Build supported organ/paper-module lookup keys for capsule compression."""
    index: dict[str, dict[str, Any]] = {}
    for row in capsules:
        capsule_id = str(row.get("id") or "").strip()
        legacy_projection = str(row.get("legacy_markdown_projection") or "").strip()
        keys = {
            capsule_id,
            f"{PAPER_MODULE_CAPSULES_REL}#{capsule_id}" if capsule_id else "",
            legacy_projection,
        }
        for subject in row.get("subjects") or []:
            if not isinstance(subject, dict):
                continue
            if subject.get("kind") == "organ" and subject.get("ref"):
                ref = str(subject.get("ref"))
                keys.add(ref)
                keys.add(f"paper_module.{ref}")
        for key in keys:
            if key:
                index.setdefault(key, row)
    return index


def _capsule_for_gloss(
    root: Path,
    organ_id: str,
    gloss: dict[str, Any],
    capsule_by_key: dict[str, dict[str, Any]],
) -> tuple[dict[str, Any] | None, str]:
    declared = str(gloss.get("paper_module_ref") or "").strip()
    lookup_keys: list[str] = []
    if declared:
        lookup_keys.append(declared)
        declared_path, _, fragment = declared.partition("#")
        if fragment:
            lookup_keys.extend([fragment, f"{declared_path}#{fragment}"])
        if declared_path:
            lookup_keys.append(declared_path)
    resolved_module = _paper_module_ref(root, organ_id, gloss)
    if resolved_module:
        lookup_keys.append(resolved_module)
    lookup_keys.extend([f"paper_module.{organ_id}", organ_id])

    seen: set[str] = set()
    for key in lookup_keys:
        if not key or key in seen:
            continue
        seen.add(key)
        row = capsule_by_key.get(key)
        if not row:
            continue
        capsule_id = str(row.get("id") or "")
        status = "direct"
        if capsule_id != f"paper_module.{organ_id}":
            status = "paper_module_ref_bridge"
        return row, status
    return None, "missing"


def _capsule_subject_refs(row: dict[str, Any], kind: str) -> set[str]:
    refs: set[str] = set()
    for subject in row.get("subjects") or []:
        if isinstance(subject, dict) and subject.get("kind") == kind and subject.get("ref"):
            refs.add(str(subject.get("ref")))
    return refs


def _organ_doctrine_by_id(root: Path) -> dict[str, dict[str, Any]]:
    """Projected concept/mechanism rows keyed by accepted organ id."""
    return {
        str(row.get("organ_id")): row
        for row in build_organ_doctrine_rows(root)
        if isinstance(row, dict) and row.get("organ_id")
    }


def _first_n_sorted(values: set[str], limit: int) -> list[str]:
    return sorted(value for value in values if value)[:limit]


def _organ_topology_command(*parts: str) -> str:
    """Render a shell-safe public drilldown command."""
    return " ".join(
        shlex.quote(part) for part in ("microcosm", "organ-topology", *parts)
    )


def _edge_sort_key(edge: dict[str, Any]) -> tuple[int, str, str, str, str]:
    relation_type = str(edge.get("relation_type") or "")
    return (
        SOURCE_RELATION_PRIORITY.get(relation_type, 99),
        str(edge.get("source_ref") or ""),
        str(edge.get("target_ref") or ""),
        str(edge.get("source_shard_ref") or ""),
        str(edge.get("validation_ref") or ""),
    )


def _source_relation_edge_handle(edge: dict[str, Any]) -> dict[str, Any]:
    """Compact, body-free handle for one source-module relation edge."""
    keys = (
        "relation_type",
        "source_ref",
        "target_ref",
        "source_shard_ref",
        "target_shard_ref",
        "required_anchor",
        "validation_ref",
    )
    return {key: edge.get(key) for key in keys if edge.get(key)}


def _source_relation_summary_for_organ(
    organ_id: str,
    edges: list[dict[str, Any]],
) -> dict[str, Any]:
    relation_counts = Counter(
        str(edge.get("relation_type") or "") for edge in edges if edge.get("relation_type")
    )
    source_refs = {
        str(edge.get("source_ref") or "")
        for edge in edges
        if edge.get("source_ref")
    }
    target_refs = {
        str(edge.get("target_ref") or "")
        for edge in edges
        if edge.get("target_ref")
    }
    source_shard_refs = {
        str(edge.get("source_shard_ref") or "")
        for edge in edges
        if edge.get("source_shard_ref")
    }
    target_shard_refs = {
        str(edge.get("target_shard_ref") or "")
        for edge in edges
        if edge.get("target_shard_ref")
    }
    validation_refs = {
        str(edge.get("validation_ref") or "")
        for edge in edges
        if edge.get("validation_ref")
    }
    top_source_refs = _first_n_sorted(source_refs, 5)
    top_target_refs = _first_n_sorted(target_refs, 5)
    top_source_shard_refs = _first_n_sorted(source_shard_refs, 3)
    top_target_shard_refs = _first_n_sorted(target_shard_refs, 3)
    top_validation_refs = _first_n_sorted(validation_refs, 3)

    query_examples = [_organ_topology_command("--organ", organ_id)]
    if top_source_refs:
        query_examples.append(
            _organ_topology_command(
                "--relation-type",
                "file_to_file",
                "--source-ref",
                top_source_refs[0],
            )
        )
    if top_source_shard_refs:
        query_examples.append(
            _organ_topology_command(
                "--relation-type",
                "shard_to_shard",
                "--shard-ref",
                top_source_shard_refs[0],
            )
        )
    if top_validation_refs:
        query_examples.append(
            _organ_topology_command("--validation-ref", top_validation_refs[0])
        )

    return {
        "organ_id": organ_id,
        "source": SOURCE_MODULE_FILE_GRAPH_REF,
        "authority_boundary": (
            "Body-free source-module relation handles only; use organ-topology "
            "for dynamic edge drilldown, not this route row as source authority."
        ),
        "output_schema": ORGAN_TOPOLOGY_SCHEMA,
        "edge_count": len(edges),
        "source_ref_count": len(source_refs),
        "target_ref_count": len(target_refs),
        "source_shard_ref_count": len(source_shard_refs),
        "target_shard_ref_count": len(target_shard_refs),
        "validation_ref_count": len(validation_refs),
        "relation_type_counts": dict(sorted(relation_counts.items())),
        "top_source_refs": top_source_refs,
        "top_target_refs": top_target_refs,
        "top_source_shard_refs": top_source_shard_refs,
        "top_target_shard_refs": top_target_shard_refs,
        "top_validation_refs": top_validation_refs,
        "query_examples": query_examples,
        "example_edges": [
            _source_relation_edge_handle(edge)
            for edge in sorted(edges, key=_edge_sort_key)[:3]
        ],
    }


def _source_relation_summaries_by_organ(root: Path) -> dict[str, dict[str, Any]]:
    """Build compact source-module relation handles keyed by organ id."""
    payload = build_organ_surface_contract(root)
    file_graph = payload["coverage"]["source_module_file_graph"]
    edges_by_organ = file_graph.get("edges_by_organ", {})
    if not isinstance(edges_by_organ, dict):
        return {}
    summaries: dict[str, dict[str, Any]] = {}
    for organ_id, raw_edges in sorted(edges_by_organ.items()):
        edges = [edge for edge in raw_edges if isinstance(edge, dict)]
        if not edges:
            continue
        summaries[str(organ_id)] = _source_relation_summary_for_organ(
            str(organ_id),
            edges,
        )
    return summaries


def _route_source_relation_summary(
    task_class: str,
    organ_summaries: list[dict[str, Any]],
) -> dict[str, Any]:
    relation_counts: Counter[str] = Counter()
    source_refs: set[str] = set()
    target_refs: set[str] = set()
    source_shard_refs: set[str] = set()
    target_shard_refs: set[str] = set()
    validation_refs: set[str] = set()
    source_ref_count = 0
    target_ref_count = 0
    source_shard_ref_count = 0
    target_shard_ref_count = 0
    validation_ref_count = 0
    example_edges: list[dict[str, Any]] = []
    query_examples: list[str] = []

    for summary in organ_summaries:
        relation_counts.update(summary.get("relation_type_counts") or {})
        source_ref_count += int(summary.get("source_ref_count") or 0)
        target_ref_count += int(summary.get("target_ref_count") or 0)
        source_shard_ref_count += int(summary.get("source_shard_ref_count") or 0)
        target_shard_ref_count += int(summary.get("target_shard_ref_count") or 0)
        validation_ref_count += int(summary.get("validation_ref_count") or 0)
        source_refs.update(str(ref) for ref in summary.get("top_source_refs") or [])
        target_refs.update(str(ref) for ref in summary.get("top_target_refs") or [])
        source_shard_refs.update(
            str(ref) for ref in summary.get("top_source_shard_refs") or []
        )
        target_shard_refs.update(
            str(ref) for ref in summary.get("top_target_shard_refs") or []
        )
        validation_refs.update(
            str(ref) for ref in summary.get("top_validation_refs") or []
        )
        example_edges.extend(
            edge for edge in summary.get("example_edges") or [] if isinstance(edge, dict)
        )
        for example in summary.get("query_examples") or []:
            example_text = str(example)
            if example_text and example_text not in query_examples:
                query_examples.append(example_text)

    top_source_refs = _first_n_sorted(source_refs, 5)
    top_source_shard_refs = _first_n_sorted(source_shard_refs, 3)
    top_validation_refs = _first_n_sorted(validation_refs, 3)
    if top_source_refs:
        example = _organ_topology_command(
            "--relation-type",
            "file_to_file",
            "--source-ref",
            top_source_refs[0],
        )
        if example not in query_examples:
            query_examples.append(example)
    if top_source_shard_refs:
        example = _organ_topology_command(
            "--relation-type",
            "shard_to_shard",
            "--shard-ref",
            top_source_shard_refs[0],
        )
        if example not in query_examples:
            query_examples.append(example)
    if top_validation_refs:
        example = _organ_topology_command("--validation-ref", top_validation_refs[0])
        if example not in query_examples:
            query_examples.append(example)

    return {
        "task_class": task_class,
        "source": SOURCE_MODULE_FILE_GRAPH_REF,
        "authority_boundary": (
            "Body-free aggregate over relevant organs; dynamic edge truth remains "
            "in organ-topology."
        ),
        "output_schema": ORGAN_TOPOLOGY_SCHEMA,
        "organ_with_source_relation_count": len(organ_summaries),
        "edge_count": sum(int(summary.get("edge_count") or 0) for summary in organ_summaries),
        "source_ref_count": source_ref_count,
        "target_ref_count": target_ref_count,
        "source_shard_ref_count": source_shard_ref_count,
        "target_shard_ref_count": target_shard_ref_count,
        "validation_ref_count": validation_ref_count,
        "relation_type_counts": dict(sorted(relation_counts.items())),
        "top_source_refs": top_source_refs,
        "top_target_refs": _first_n_sorted(target_refs, 5),
        "top_source_shard_refs": top_source_shard_refs,
        "top_target_shard_refs": _first_n_sorted(target_shard_refs, 3),
        "top_validation_refs": top_validation_refs,
        "query_examples": query_examples[:5],
        "example_edges": example_edges[:5],
    }


def _source_relation_route_handle(
    organ_id: str,
    summary: dict[str, Any] | None,
) -> dict[str, Any] | None:
    """Small per-organ pointer for generated route rows."""
    if not isinstance(summary, dict):
        return None
    query_examples = summary.get("query_examples") or []
    return {
        "source_relation_ref": (
            f"{SOURCE_MODULE_FILE_GRAPH_REF}.edges_by_organ"
            f"[organ_id={organ_id}]"
        ),
        "edge_count": summary.get("edge_count", 0),
        "source_ref_count": summary.get("source_ref_count", 0),
        "target_ref_count": summary.get("target_ref_count", 0),
        "source_shard_ref_count": summary.get("source_shard_ref_count", 0),
        "target_shard_ref_count": summary.get("target_shard_ref_count", 0),
        "validation_ref_count": summary.get("validation_ref_count", 0),
        "query": query_examples[0] if query_examples else "",
    }


def _source_relation_md_line(card: dict[str, Any]) -> str:
    organ_id = str(card["organ_id"])
    summary = card.get("source_relation_summary")
    query = _organ_topology_command("--organ", organ_id)
    if not isinstance(summary, dict):
        return (
            "no source-module file/shard edges recorded in "
            f"`{SOURCE_MODULE_FILE_GRAPH_REF}` · drilldown `{query}`"
        )

    query_examples = [str(example) for example in summary.get("query_examples") or []]
    if query_examples:
        query = query_examples[0]
    source_ref = (
        f"{SOURCE_MODULE_FILE_GRAPH_REF}.edges_by_organ[organ_id={organ_id}]"
    )
    bits = [
        f"`{int(summary.get('edge_count') or 0)}` source-module edges",
        f"`{int(summary.get('source_ref_count') or 0)}` source files",
        f"`{int(summary.get('source_shard_ref_count') or 0)}` source shards",
        f"`{int(summary.get('target_ref_count') or 0)}` target files",
        f"`{int(summary.get('target_shard_ref_count') or 0)}` target shards",
        f"handle `{_md_cell(source_ref)}`",
        f"drilldown `{_md_cell(query)}`",
    ]
    return " · ".join(bits)


def _architecture_source_relation_summary(model: dict[str, Any]) -> dict[str, Any]:
    """Aggregate body-free source relation handles across all accepted organs."""
    summaries = [
        summary
        for family in model.get("families", [])
        for card in family.get("cards", [])
        for summary in [card.get("source_relation_summary")]
        if isinstance(summary, dict)
    ]
    return _route_source_relation_summary("architecture", summaries)


def _architecture_source_relation_commands(summary: dict[str, Any]) -> list[str]:
    """Pick stable architecture drilldowns from the aggregate relation summary."""
    commands: list[str] = []
    for example in summary.get("query_examples") or []:
        command = str(example)
        if command and command not in commands:
            commands.append(command)

    top_source_refs = [str(ref) for ref in summary.get("top_source_refs") or []]
    top_source_shard_refs = [
        str(ref) for ref in summary.get("top_source_shard_refs") or []
    ]
    top_validation_refs = [
        str(ref) for ref in summary.get("top_validation_refs") or []
    ]
    candidates = []
    if top_source_refs:
        candidates.append(
            _organ_topology_command(
                "--relation-type",
                "file_to_file",
                "--source-ref",
                top_source_refs[0],
            )
        )
    if top_source_shard_refs:
        candidates.append(
            _organ_topology_command(
                "--relation-type",
                "shard_to_shard",
                "--shard-ref",
                top_source_shard_refs[0],
            )
        )
    if top_validation_refs:
        candidates.append(
            _organ_topology_command("--validation-ref", top_validation_refs[0])
        )

    for command in candidates:
        if command not in commands:
            commands.append(command)
    return commands[:5]


def load_model(root: str | Path) -> dict[str, Any]:
    """Build the merged, validated atlas model from public substrate.

    Returns a dict with ``status`` ("pass"/"blocked"), ``blocking_reasons``,
    the merged ``families`` (each with resolved organ cards), the evidence-class
    legend, the kernel primitives, and coverage counts. Fails closed: any
    registry/family/gloss mismatch or empty gloss field blocks the model.
    """
    root = Path(root)
    registry_rows = _accepted_registry_rows(root)
    registry_by_id = {str(r.get("organ_id")): r for r in registry_rows}
    registry_ids = set(registry_by_id)

    families_doc = read_json_strict(root / FAMILIES_REL)
    families = [f for f in families_doc.get("families", []) if isinstance(f, dict)]

    glosses_doc: dict[str, Any]
    glosses_path = root / GLOSSES_REL
    if glosses_path.is_file():
        glosses_doc = read_json_strict(glosses_path)
    else:
        glosses_doc = {"organs": []}
    gloss_by_id = {
        str(g.get("organ_id")): g
        for g in glosses_doc.get("organs", [])
        if isinstance(g, dict)
    }
    paper_module_capsules = _paper_module_capsules(root)
    capsule_by_key = _capsule_index(paper_module_capsules)

    evidence_doc = read_json_strict(root / EVIDENCE_CLASSES_REL)
    class_profiles = (
        evidence_doc.get("class_profiles", {})
        if isinstance(evidence_doc, dict)
        else {}
    )
    evidence_class_by_organ = {
        str(r.get("organ_id")): str(r.get("evidence_class"))
        for r in _rows(evidence_doc, "organ_evidence_classes")
    }
    substrate_doc: dict[str, Any] = {}
    substrate_path = root / SUBSTRATE_LEDGER_REL
    if substrate_path.is_file():
        substrate_doc = read_json_strict(substrate_path)
    substrate_by_id = {
        str(r.get("organ_id")): r
        for r in _rows(substrate_doc, "organ_substrate_dispositions")
    }
    doctrine_by_id = _organ_doctrine_by_id(root)
    source_relation_by_organ = _source_relation_summaries_by_organ(root)

    kernel_doc = read_json_strict(root / KERNEL_REL)
    kernel_primitives = _rows(kernel_doc, "primitives")

    # --- coverage validation (fail closed) ---
