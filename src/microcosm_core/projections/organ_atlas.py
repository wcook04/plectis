def build(root: str | Path, *, write: bool) -> dict[str, Any]:
    """
    [ACTION]
    Build the atlas model and optionally write generated atlas surfaces.

    In ``--check`` callers, pass ``write=False`` and compare the returned
    rendered outputs to the on-disk files.

    - Teleology: the single build/check entrypoint that renders all four
      generated atlas surfaces and either writes them or reports drift.
    - Guarantee: returns a dict with ``status``, ``blocking_reasons``,
      ``coverage``, ``drift`` (files differing from on-disk), ``wrote`` (paths
      written), and the rendered outputs. Writes ORGANS.md, ARCHITECTURE.md,
      AGENT_ROUTES.md, and ``atlas/agent_task_routes.json`` ONLY when
      ``write=True`` AND ``model['status'] == 'pass'``; on write, ``drift`` is
      cleared.
    - Fails: never raises on honesty failures (a blocked model writes nothing and
      returns ``status='blocked'``); raises via ``load_model`` only on missing or
      malformed ``core/*.json`` sources.
    - When-needed: inspect when generating the atlas (``--write``) or running the
      drift gate (``--check``).
    - Reads: the source registries via ``load_model``; existing on-disk generated
      files for drift comparison.
    - Writes: ``ORGANS.md``, ``ARCHITECTURE.md``, ``AGENT_ROUTES.md``,
      ``atlas/agent_task_routes.json`` (only when write=True and status passes).
    - Escalates-to: ``scripts/build_organ_atlas.py`` (CLI wrapper) and
      ``microcosm_core.validators.public_entry_docs`` (the drift gate).
    - Non-goal: writing the files does not authorize release, hosted deployment,
      provider calls, source mutation, private-root equivalence, or proof
      authority above each organ's own claim ceiling.
    - Preconditions: Caller supplies arguments satisfying the signature plus any path, schema, state, or type constraints enforced by the body.
    """
    root = Path(root)
    model = load_model(root)
    model["runtime_witness"] = _load_runtime_witness(root)
    organs_md = render_organs_md(model)
    architecture_md = render_architecture_md(model)
    agent_routes_md = render_agent_routes_md(model)
    agent_task_routes = render_agent_task_routes_json(model)
    agent_task_routes_json = (
        json.dumps(agent_task_routes, ensure_ascii=True, indent=2, sort_keys=True)
        + "\n"
    )
    on_disk_organs = (root / ORGANS_MD_REL).read_text(encoding="utf-8") if (root / ORGANS_MD_REL).is_file() else None
    on_disk_arch = (root / ARCHITECTURE_MD_REL).read_text(encoding="utf-8") if (root / ARCHITECTURE_MD_REL).is_file() else None
    on_disk_agent_routes = (root / AGENT_ROUTES_MD_REL).read_text(encoding="utf-8") if (root / AGENT_ROUTES_MD_REL).is_file() else None
    agent_task_routes_path = root / AGENT_TASK_ROUTES_JSON_REL
    on_disk_agent_task_routes = agent_task_routes_path.read_text(encoding="utf-8") if agent_task_routes_path.is_file() else None
    drift = []
    if on_disk_organs is not None and on_disk_organs != organs_md:
        drift.append(ORGANS_MD_REL)
    if on_disk_arch is not None and on_disk_arch != architecture_md:
        drift.append(ARCHITECTURE_MD_REL)
    if on_disk_agent_routes is not None and on_disk_agent_routes != agent_routes_md:
        drift.append(AGENT_ROUTES_MD_REL)
    if (
        on_disk_agent_task_routes is not None
        and on_disk_agent_task_routes != agent_task_routes_json
    ):
        drift.append(AGENT_TASK_ROUTES_JSON_REL)
    wrote: list[str] = []
    if write and model["status"] == "pass":
        (root / ORGANS_MD_REL).write_text(organs_md, encoding="utf-8")
        (root / ARCHITECTURE_MD_REL).write_text(architecture_md, encoding="utf-8")
        (root / AGENT_ROUTES_MD_REL).write_text(agent_routes_md, encoding="utf-8")
        agent_task_routes_path.parent.mkdir(parents=True, exist_ok=True)
        agent_task_routes_path.write_text(agent_task_routes_json, encoding="utf-8")
        wrote = [
            ORGANS_MD_REL,
            ARCHITECTURE_MD_REL,
            AGENT_ROUTES_MD_REL,
            AGENT_TASK_ROUTES_JSON_REL,
        ]
        drift = []
    return {
        "status": model["status"],
        "blocking_reasons": model["blocking_reasons"],
        "coverage": model["coverage"],
        "drift": drift,
        "wrote": wrote,
        "organs_md": organs_md,
        "architecture_md": architecture_md,
        "agent_routes_md": agent_routes_md,
        "agent_task_routes": agent_task_routes,
        "agent_task_routes_json": agent_task_routes_json,
    }
